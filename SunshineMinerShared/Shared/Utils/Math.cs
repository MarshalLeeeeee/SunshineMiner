using System.Collections;
using System.Collections.Generic;

public class Vec2
{
    public float x;
    public float y;

    public Vec2()
    {
        x = 0;
        y = 0;
    }

    public Vec2(float x_, float y_)
    {
        x = x_;
        y = y_;
    }
}

public class Vec3
{
    public float x;
    public float y;
    public float z;

    public Vec3()
    {
        x = 0;
        y = 0;
        z = 0;
    }

    public Vec3(float x_, float y_, float z_)
    {
        x = x_;
        y = y_;
        z = z_;
    }
}
